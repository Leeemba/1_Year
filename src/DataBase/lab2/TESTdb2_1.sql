SELECT
COUNT(DISTINCT Н_УЧЕНИКИ.ЧЛВК_ИД) AS число_отличников
FROM
Н_УЧЕНИКИ
 JOIN Н_ВЕДОМОСТИ ON Н_ВЕДОМОСТИ.ЧЛВК_ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
 JOIN Н_ПЛАНЫ ON Н_УЧЕНИКИ.ПЛАН_ИД = Н_ПЛАНЫ.ИД
 JOIN Н_ОТДЕЛЫ ON Н_ОТДЕЛЫ.ИД = Н_ПЛАНЫ.ОТД_ИД
 AND Н_ОТДЕЛЫ.КОРОТКОЕ_ИМЯ = 'СПбГУИТМО'
WHERE
NOT EXISTS (
 SELECT
 *
 FROM
 Н_ВЕДОМОСТИ
 WHERE
 Н_ВЕДОМОСТИ.ЧЛВК_ИД = Н_УЧЕНИКИ.ЧЛВК_ИД
 AND Н_ВЕДОМОСТИ.ОЦЕНКА NOT IN ('зачет', 'осв', '5') 